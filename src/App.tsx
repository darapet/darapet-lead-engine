import { AppLayout } from "./components/layout/AppLayout";
import { Dashboard } from "./pages/dashboard";
import { NewLeadBatch } from "./pages/leads/new";
import { LeadHistory } from "./pages/leads/history";
import { LeadBatchDetail } from "./pages/leads/detail";
import { SettingsPage } from "./pages/settings";
import { QueryClient, QueryClientProvider } from '@tanstack/react-query';
import { Toaster } from '@/components/ui/toaster';
import { TooltipProvider } from '@/components/ui/tooltip';
import NotFound from '@/pages/not-found';
import { Route, Switch, Router as WouterRouter } from 'wouter';

const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      refetchOnWindowFocus: false,
      retry: 1,
    },
  },
});

function Router() {
  return (
    <AppLayout>
      <Switch>
        <Route path="/" component={Dashboard} />
        <Route path="/leads/new" component={NewLeadBatch} />
        <Route path="/leads" component={LeadHistory} />
        <Route path="/leads/:id" component={LeadBatchDetail} />
        <Route path="/settings" component={SettingsPage} />
        <Route component={NotFound} />
      </Switch>
    </AppLayout>
  );
}

function App() {
  return (
    <QueryClientProvider client={queryClient}>
      <TooltipProvider>
        <WouterRouter base={import.meta.env.BASE_URL?.replace(/\/$/, '')}>
          <Router />
        </WouterRouter>
        <Toaster />
      </TooltipProvider>
    </QueryClientProvider>
  );
}

export default App;
